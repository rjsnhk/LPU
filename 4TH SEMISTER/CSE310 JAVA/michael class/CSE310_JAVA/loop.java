public class loop {
    public static void main(String[] args) {
        int w=0;
        int d=0;
        for(int i=0;i<5;i++){
            System.out.println("for");
        }
        while(w<5){
            System.out.println("while ");
            w++;
        }
        do{
            System.out.println("do-while ");
            d++;
        }while(d<6);
    }
}
